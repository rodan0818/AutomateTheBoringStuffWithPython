#There are two boolean data types i.e True , False
#If an expression is true then its boolean data type is True.For example, 
# == operator checks if two values are equal or identical or not
#In 42==42 both are equal so its boolean type is True
print('Boolean type of 42==42 is ',42==42)

#Lets do an example when boolean data type is false
#In 42==43 both are not equal so its boolean type is False
print('Boolean type of 42==43 is ',42==43)
