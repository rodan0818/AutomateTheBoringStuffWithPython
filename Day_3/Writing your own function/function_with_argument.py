def hello(name): # Function definition that takes one argument from calling statement (Single parameter function)
    print('Hello '+name)

def hello2(name,age): # Function definition that takes two argument from calling statement (double parameter function)
    print('Hi '+name,'\n','Your age is '+age+'\n')

hello('Adam') # Calling function with passing one argument
hello('Steve')
print('\n')
hello2('Adam','12') # Calling function with passing one argument
hello2('Steve','15')

